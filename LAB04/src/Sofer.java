public class Sofer {
    private String nume;
    private String prenume;
    private int varsta;
    private int nr_permis;
    public Sofer(String nume, String prenume, int varsta, int nr_permis) {
        this.nume = nume;
        this.prenume = prenume;
        this.varsta = varsta;
        this.nr_permis = nr_permis;
    }
}
